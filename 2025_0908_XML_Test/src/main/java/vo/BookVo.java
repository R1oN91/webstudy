package vo;

public class BookVo {

	String title;
	String image;
	String name;
	String link;
	String chul;
	String catego;
	int price;
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLink() {
		return link;
	}
	public void setLink(String link) {
		this.link = link;
	}
	public String getChul() {
		return chul;
	}
	public void setChul(String chul) {
		this.chul = chul;
	}
	public String getCatego() {
		return catego;
	}
	public void setCatego(String catego) {
		this.catego = catego;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	
	
	
}
